     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate(["description_en"=>"required","description_ar"=>"required"]);

        $aboutUs=AboutUs::findOrFail($id);

